function twentyfifteen_scripts() {

	// Load our main stylesheet.
	wp_enqueue_style( 'twentyfifteen-style', get_template_directory_uri() . '/style.css', array(), time() );

	wp_enqueue_script( 'twentyfifteen-script', get_template_directory_uri() . '/js/functions.js', array( 'jquery' ), time() );
}
add_action( 'wp_enqueue_scripts', 'twentyfifteen_scripts' );

